import java.awt.*;

public class Vertex {
    double x, y, z;
    double r, g, b;

    public Vertex(double x, double y, double z, double r, double g, double b) {
        //Coordinates
        this.x = x;
        this.y = y;
        this.z = z;

        //Colours
        this.r = r;
        this.g = g;
        this.b = b;
    }
}
